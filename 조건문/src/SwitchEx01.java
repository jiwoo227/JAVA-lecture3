public class SwitchEx01 {
    public static void main(String[] args){
        //switch, case, default, break

        int value = 1;

        switch (value){
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("2");
                break;
            default:
                System.out.println("그 외 다른 숫자");
                break;
        }
    }
}
